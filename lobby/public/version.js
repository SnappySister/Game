/* ============================================================================
 * 游戏版本号 - 前后端共享，改版本号只需改这一处
 * ============================================================================
 *
 * 【格式】语义化版本(SemVer)：主版本.次版本.修订号  (X.Y.Z)
 *
 * 【升级规则】每次发版前根据本次改动类型决定升哪一位：
 *
 *   修订号 Z+1 (向后兼容的修复/调整，不影响玩法结构)
 *     · 修复bug          例: 修复崩溃问题、胡牌bug
 *     · 平衡性/数值调整   例: 调整卡牌数值、削弱赌徒
 *     · 显示/文案优化     例: 显示装备和随从名字、规则描述更新
 *
 *   次版本 Y+1 (向后兼容的新内容/新功能)
 *     · 新增卡牌/英雄     例: 新增英雄财阀和不死族、增加12张新卡
 *     · 新增系统/机制     例: 引入装备随从系统、稀有度抽牌
 *     · 新游戏上线        例: 大富翁上线
 *     · 新增玩法功能      例: 回合超时、重连机制
 *
 *   主版本 X+1 (不兼容的破坏性变更，旧客户端必须升级)
 *     · 协议/数据结构变更 例: 引入账号系统、数据持久化改造
 *     · 大规模重写       例: 游戏引擎重构
 *
 * 【判定原则】有疑问时按"就高不就低"——改动同时涉及多类时取最高位。
 *
 * 【发版流程】
 *   1. 修改下方 VERSION 字符串
 *   2. 提交代码并推送 (前后端共享此文件，一处改动两边同步)
 *   3. 打 git tag 锚定版本：
 *        git tag -a v1.0.0 -m "首个正式版本：大厅+4游戏+角色系统"
 *        git push origin v1.0.0
 *
 * 【校验机制】服务器在连接时下发 serverVersion，登录时校验客户端 clientVersion；
 *   不一致时记录日志并提示玩家刷新，避免旧前端连上新后端出问题。
 *   注意：静态文件已设 no-cache，确保玩家拿到最新前端，校验才有效。
 * ========================================================================== */
